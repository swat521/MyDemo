package vip.pk.lib.splash;

public class SplashBean {

	private int verCode;
	private String verShow;
	private String verDownUrl;
	private String verDesc;
	public int getVerCode() {
		return verCode;
	}
	public void setVerCode(int verCode) {
		this.verCode = verCode;
	}
	public String getVerShow() {
		return verShow;
	}
	public void setVerShow(String verShow) {
		this.verShow = verShow;
	}
	public String getVerDownUrl() {
		return verDownUrl;
	}
	public void setVerDownUrl(String verDownUrl) {
		this.verDownUrl = verDownUrl;
	}
	public String getVerDesc() {
		return verDesc;
	}
	public void setVerDesc(String verDesc) {
		this.verDesc = verDesc;
	}
	
	
	
	
}
